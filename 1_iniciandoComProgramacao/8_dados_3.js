let idade = 31; //essa informação é numérica, mas conseguimos confirmar o tipo de informação dentro de uma determinada string da seguinte forma: 
console.log(typeof idade); // utiliza-se a sintaxe [typeof] [variável]

let salario = 4568.32
console.log(typeof salario);
//algumas linguagens de programação separam numeros em tipos diferentes, a depender se são cheios ou não, por exemplo. No caso do JS, tudo é number.

let estaChovendo = true; //variavel booleana (só aceita true ou false)
console.log(typeof estaChovendo);

let textoDeTeste = 'Arthur' //variavel do tipo string
console.log(typeof textoDeTeste);

//esses são os 3 tipos basicos de dados aceitos pelo javascript.
//há outros tipos de variável em JS, mas são mais específicos e serão abordados mais para frente.